#Arithmetics operators

""""
sum      (+)
rest     (-)
div      (/)
div int  (//)
mul      (*)
mod      (%)
exp      (**)
"""

sum_result = 2 + 2
res_result = 2 - 2
div_result = 4 / 2
div_int_result = 5 // 2
mul_result = 2 * 2
mod_result = 5 % 2
exp_result = 2 ** 3

print()
print('********** operations with Arithmetics Operators *********')
print(f'sum : {sum_result}')
print(f'res: {res_result}')
print(f'div: {div_result}')
print(f'div_int: {div_int_result}')
print(f'mul: {mul_result}')
print(f'mod: {mod_result}')
print(f'exp: {exp_result}')


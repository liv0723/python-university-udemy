# special character in str

#this character is using '\'
# \n insert a new line
# \t insert a tab, by align the text
# \' insert ' in one str delimited by ''
# \" insert " in one str delimited by "
# \\ insert \ in one str

print("Hello \nworld")
print("Hello \twold")
print('\' Hello world \'')
print("\" Hello world \"")
print("Hello \\ world")
